        <h2>Software Alternatives</h2>
        <p><br>
          <strong>Operating systems</strong></p>
  	      I use Gentoo GNU/Linux. Avoid Windows and macOS, instead use any GNU/Linux distribution or *BSD variant. If you want a 100% free system check out the <a href="https://www.gnu.org/distros/free-distros.en.html">FSF endorsed distributions</a><br><br>
	        <strong>VPNs</strong><br>
	        Don't use one, instead use Tor or I2P, if thats not an option; <a href="https://www.ivpn.net/">IVPN</a>, <a href="https://www.ovpn.com/">OVPN</a> or <a href="https://mullvad.net">Mullvad VPN</a> preferably via Monero or cash<br><br>
	        <strong>Modern browsers</strong><br>
  	      If you like Firefox switch to <a href="https://librewolf.net/">LibreWolf</a> or use a custom user.js, if you like Chrome switch to <a href="https://github.com/Eloston/ungoogled-chromium">Ungoogled Chromium</a>, if you want something minimal with Vim keybinds use <a href="https://www.qutebrowser.org/">qutebrowser</a>. I've used all three, and can't recommend one specifically.<br><br>
	        <strong>Messaging services</strong><br>
	        If you want something modern with multimedia, screenshare VC and encryption use Matrix with the <a href="https://element.io/download">Element client</a>, if you don't like electron use <a href="https://github.com/Nheko-Reborn/nheko">nheko</a>. You could also use irc/xmpp.<br><br>
  	      <strong>Search engines</strong><br>
	        I personally use <a href="https://github.com/hnhx/librex">LibreX</a> <a href="https://search.ahwx.org">(instance)</a>. <a href="https://wiby.me/">Wiby</a> is also very cool.<br><br>
          <strong>Git hosts</strong><br>
          If you want to self host use <a href="https://git.zx2c4.com/cgit/">cgit</a>. Otherwise everything isn't great, <a href="https://disroot.org">Disroot</a> looks promising.<br><br>
          <strong>Code editors</strong><br>
          I personally use <a href="https://neovim.io/">Neovim</a>. If you need a GUI, I'd recommend <a href="https://vscodium.com">VS Codium</a> (libre VSCode).<br><br>
